/**
 *  获取当前程序运行的iOS系统的版本号
 **/
+(NSString*)iOSSystemVersion
{
    
    NSString *sysVersion = [UIDevice currentDevice].systemVersion;
    return sysVersion;
}

/**
 *  获取应用的UUID
 **/
+(NSString *)AppUUIDString
{
    return [UIDevice currentDevice].identifierForVendor.UUIDString;
}

